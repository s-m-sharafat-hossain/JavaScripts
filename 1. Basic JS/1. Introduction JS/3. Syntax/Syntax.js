
            //Write in the web page:

document.write("<br>hi");
// We can use HTML tag in document.write.
document.write("<br>");
document.write(45+45);
document.write("<br>");

//We can also use single quotation for print string.
document.writeln('hi');
document.writeln("<br>");
//Writeln for new line.
document.writeln(45+45);
document.writeln("<br>");

//We can use backtick(`) for string and expression.
document.write(`hi ${45+45} <br>`);


            //Write in the console:

console.log("hi");
console.log("<br>");
console.log(45+45);
console.log("<br>");

//We can use backtick(`) for string and expression.
console.log(`hi <b>${45+45}</b>`);
console.log("<br>");



