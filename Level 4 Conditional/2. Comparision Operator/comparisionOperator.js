/*
=> Comparison Operators: 

-> Equality: 
- == Checks values equality
- === checks values and type equality.

-> Inequality:
- != Not equal
- !== value and type not equal

-> Relational: 
- > Grater than
- < Less than
- >= Greater than or equal to.
- <= Less than or equal to.
*/

